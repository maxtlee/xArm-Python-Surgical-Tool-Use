def within_workspace(pos_mm: np.ndarray) -> bool:
    bounds = WORKSPACE_BOUNDS
    return (
        bounds["x"][0] <= pos_mm[0] <= bounds["x"][1] and
        bounds["y"][0] <= pos_mm[1] <= bounds["y"][1] and
        bounds["z"][0] <= pos_mm[2] <= bounds["z"][1]
    )


