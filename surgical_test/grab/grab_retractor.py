def draw_overlay(frame, rvec, tvec, corner_set, stable, cam_matrix, dist_coeffs,
                 target_mm=None):
    overlay = frame.copy()

    if rvec is not None and tvec is not None:
        # Draw the 3D coordinate axes on the marker face
        cv2.drawFrameAxes(overlay, cam_matrix, dist_coeffs, rvec, tvec,
                          MARKER_SIZE_M / 2)

        # Highlight the detected marker border
        if corner_set is not None:
            pts = corner_set.reshape(4, 2).astype(int)
            cv2.polylines(overlay, [pts], isClosed=True,
                          color=(0, 255, 0), thickness=2)
            cx = int(pts[:, 0].mean())
            cy = int(pts[:, 1].mean())
            cv2.circle(overlay, (cx, cy), 5, (0, 255, 0), -1)

        # Stability indicator
        if stable:
            status_text  = "STABLE  —  press G to hover"
            status_color = (0, 255, 0)
        else:
            filled = len([1 for _ in range(1)])   # just for layout
            status_text  = f"Acquiring stability …"
            status_color = (0, 200, 255)
    else:
        status_text  = f"Marker ID {MARKER_ID} not detected"
        status_color = (0, 0, 255)

    cv2.putText(overlay, status_text, (10, 30),
                cv2.FONT_HERSHEY_SIMPLEX, 0.7, status_color, 2)
    cv2.putText(overlay, "G=hover  R=reset  H=home  Q=quit",
                (10, STREAM_HEIGHT - 10),
                cv2.FONT_HERSHEY_SIMPLEX, 0.5, (200, 200, 200), 1)

    if target_mm is not None:
        info = (f"Target (base): "
                f"x={target_mm[0]:.1f}  y={target_mm[1]:.1f}  z={target_mm[2]:.1f} mm")
        cv2.putText(overlay, info, (10, 60),
                    cv2.FONT_HERSHEY_SIMPLEX, 0.55, (255, 255, 100), 2)

    return overlay


