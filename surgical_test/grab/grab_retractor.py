#!/usr/bin/env python3
"""
grab_retractor.py — ArUco-guided retractor handle approach
===========================================================

TRANSFORM CHAIN
---------------
ArUco solvePnP gives T_marker_cam: the 4×4 pose of the marker in camera frame.

  T_marker_cam   — marker origin in camera frame  (from solvePnP)
  T_cam_base     — camera in robot base frame      (loaded from T_cam_to_base.npy, metres)
  T_marker_base  = T_cam_base @ T_marker_cam       — marker in robot base frame
  T_offset       = pure translation [+5 cm, 0, 0] in MARKER's local X direction
                   (ArUco X points right when facing the marker; marker is on the
                    left side of the handle, so +X reaches the handle centre)
  T_target_base  = T_marker_base @ T_offset        — grab target in robot base frame
  pos_mm         = T_target_base[:3, 3] * 1000     — position sent to xArm (mm)
  hover_z        = pos_mm[2] + HOVER_CLEARANCE_MM  — hover above before descending

ArUco marker coordinate frame (solvePnP object points convention used here):
  Corner order: top-left, top-right, bottom-right, bottom-left (standard ArUco)
  +X  points RIGHT  (when facing the marker)
  +Y  points UP
  +Z  points OUT OF the marker surface toward the camera

USAGE
-----
  python surgical_test/grab/grab_retractor.py [ARM_IP]

CONTROLS
--------
  G — grab: move to hover pose (only fires when detection is STABLE)
  R — reset stability buffer and "moved" flag
  H — go to home joint angles
  Q / ESC — quit
"""

