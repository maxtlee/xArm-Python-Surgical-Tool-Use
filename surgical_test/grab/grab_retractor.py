def open_realsense():
    pipeline = rs.pipeline()
    config   = rs.config()
    config.enable_stream(rs.stream.color, STREAM_WIDTH, STREAM_HEIGHT, rs.format.bgr8, STREAM_FPS)
    profile  = pipeline.start(config)
    intr     = (
        profile.get_stream(rs.stream.color)
               .as_video_stream_profile()
               .get_intrinsics()
    )
    print(f"[RealSense] Started {STREAM_WIDTH}×{STREAM_HEIGHT} @ {STREAM_FPS} fps")
    return pipeline, intr


def get_color_frame(pipeline) -> np.ndarray | None:
    frames = pipeline.wait_for_frames()
    cf = frames.get_color_frame()
    return np.asanyarray(cf.get_data()) if cf else None


def _build_cam_matrix_from_intr(intr) -> tuple[np.ndarray, np.ndarray]:
    """Build OpenCV camera matrix and distortion coefficients from RealSense intrinsics."""
    cam_matrix = np.array([
        [intr.fx,     0.0, intr.ppx],
        [    0.0, intr.fy, intr.ppy],
        [    0.0,     0.0,      1.0],
    ], dtype=np.float64)
    dist_coeffs = np.array(intr.coeffs, dtype=np.float64)
    return cam_matrix, dist_coeffs


# ===========================================================================
# ArUco detection and pose estimation
# ===========================================================================

def build_detector():
    dictionary = cv2.aruco.getPredefinedDictionary(ARUCO_DICT_ID)
    if hasattr(cv2.aruco, "ArucoDetector"):
        params   = cv2.aruco.DetectorParameters()
        detector = cv2.aruco.ArucoDetector(dictionary, params)
        return detector, dictionary, "new"
    else:
        params = cv2.aruco.DetectorParameters_create()
        return None, dictionary, params


def detect_target_marker(frame: np.ndarray, detector_bundle, cam_matrix, dist_coeffs):
    """
    Detect MARKER_ID in frame and estimate its 6-DOF pose via solvePnP.

    Object points follow the standard ArUco layout (Z=0 plane, centred at origin):
      corner 0 → top-left    (-X, +Y)
      corner 1 → top-right   (+X, +Y)
      corner 2 → bottom-right (+X, -Y)
      corner 3 → bottom-left (-X, -Y)

    This means +X = right, +Y = up, +Z = toward camera when facing the marker.

    Returns (rvec, tvec, corner_set) or (None, None, None) if not found.
    """
    gray = cv2.cvtColor(frame, cv2.COLOR_BGR2GRAY)
    detector, dictionary, extra = detector_bundle

    if extra == "new":
        corners, ids, _ = detector.detectMarkers(gray)
    else:
        corners, ids, _ = cv2.aruco.detectMarkers(gray, dictionary, parameters=extra)

    if ids is None:
        return None, None, None

    for corner_set, mid in zip(corners, ids.flatten()):
        if int(mid) != MARKER_ID:
            continue

        half = MARKER_SIZE_M / 2.0
        obj_pts = np.array([
            [-half,  half, 0.0],   # corner 0: top-left
            [ half,  half, 0.0],   # corner 1: top-right
            [ half, -half, 0.0],   # corner 2: bottom-right
            [-half, -half, 0.0],   # corner 3: bottom-left
        ], dtype=np.float64)

        img_pts = corner_set.reshape(4, 2).astype(np.float64)
        ok, rvec, tvec = cv2.solvePnP(obj_pts, img_pts, cam_matrix, dist_coeffs)
        if ok:
            return rvec, tvec, corner_set

    return None, None, None


