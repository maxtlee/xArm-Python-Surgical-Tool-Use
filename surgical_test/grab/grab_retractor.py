def run():
    # --- Validate calibration ---
    global T_CAM_TO_BASE
    if T_CAM_TO_BASE is None:
        calib_abs = os.path.abspath(_CALIB_PATH)
        if os.path.exists(calib_abs):
            T_CAM_TO_BASE = np.load(calib_abs)
            print(f"[grab] Loaded T_cam_to_base from {calib_abs}")
        else:
            print(
                "ERROR: T_CAM_TO_BASE is not set and calibration file not found:\n"
                f"  {calib_abs}\n"
                "Either run surgical_test/cv/cv_calibration.py first, or paste\n"
                "your 4×4 matrix directly into the T_CAM_TO_BASE variable above."
            )
            sys.exit(1)

    print("[grab] T_cam_to_base =")
    print(np.array2string(T_CAM_TO_BASE, precision=4, suppress_small=True))

    # --- Connect arm ---
    ip = sys.argv[1] if len(sys.argv) > 1 else ROBOT_IP
    arm = connect_arm(ip)

    # --- Open camera ---
    pipeline, rs_intr = open_realsense()

    if USE_REALSENSE_INTRINSICS:
        cam_matrix, dist_coeffs = _build_cam_matrix_from_intr(rs_intr)
        print(f"[grab] Using RealSense intrinsics: "
              f"fx={rs_intr.fx:.2f}  fy={rs_intr.fy:.2f}  "
              f"cx={rs_intr.ppx:.2f}  cy={rs_intr.ppy:.2f}")
    else:
        cam_matrix  = CAM_MATRIX_MANUAL
        dist_coeffs = DIST_COEFFS_MANUAL
        print("[grab] Using manually specified intrinsics.")

    detector_bundle = build_detector()
    buf    = StabilityBuffer(STABLE_FRAMES, STABILITY_THRESH_M)
    moved  = False   # prevents re-triggering until reset
    last_rvec = last_tvec = last_corner = None
    last_target_mm = None

    print(
        "\n[grab] Live feed started.\n"
        f"  Target: marker ID {MARKER_ID}, offset +{OFFSET_IN_MARKER_M[0]*100:.0f} cm in marker +X\n"
        "  G=hover  R=reset  H=home  Q/ESC=quit\n"
    )

    try:
        while True:
            frame = get_color_frame(pipeline)
            if frame is None:
                continue

            rvec, tvec, corner_set = detect_target_marker(
                frame, detector_bundle, cam_matrix, dist_coeffs)

            if rvec is not None:
                buf.update(rvec, tvec)
                last_rvec, last_tvec, last_corner = rvec, tvec, corner_set
            else:
                buf.reset()

            stable = buf.is_stable()

            # Compute the live target for display (doesn't move the arm)
            if stable and last_rvec is not None:
                mean_rvec, mean_tvec = buf.mean_pose()
                T_marker_cam  = rvec_tvec_to_T(mean_rvec, mean_tvec)
                T_marker_base = T_CAM_TO_BASE @ T_marker_cam
                T_offset      = make_offset_T(OFFSET_IN_MARKER_M)
                T_target_base = T_marker_base @ T_offset
                last_target_mm = T_target_base[:3, 3] * 1000.0
            else:
                last_target_mm = None

            display = draw_overlay(
                frame, last_rvec, last_tvec, last_corner, stable,
                cam_matrix, dist_coeffs, last_target_mm)
            cv2.imshow("grab_retractor", display)

            key = cv2.waitKey(1) & 0xFF

            # ---- G: go to hover ----
            if key == ord("g"):
                if moved:
                    print("[grab] Already moved. Press R to reset before grabbing again.")
                elif not stable:
                    print("[grab] Detection not stable yet — keep the marker still.")
                else:
                    target_mm = last_target_mm
                    print(f"\n[grab] Target in base frame: "
                          f"({target_mm[0]:.1f}, {target_mm[1]:.1f}, {target_mm[2]:.1f}) mm")

                    if not within_workspace(target_mm):
                        print(
                            f"[grab] ABORT: target {target_mm} is outside workspace bounds "
                            f"{WORKSPACE_BOUNDS}. Check calibration and marker placement."
                        )
                    else:
                        success = move_hover(arm, target_mm)
                        if success:
                            moved = True

            # ---- R: reset ----
            elif key == ord("r"):
                print("[grab] Reset.")
                buf.reset()
                moved = False
                last_target_mm = None

            # ---- H: home ----
            elif key == ord("h"):
                go_home(arm)
                moved = False

            # ---- Q / ESC: quit ----
            elif key in (ord("q"), 27):
                print("[grab] Quit.")
                break

    finally:
        pipeline.stop()
        arm.disconnect()
        cv2.destroyAllWindows()
        print("[grab] Done.")


if __name__ == "__main__":
    run()
