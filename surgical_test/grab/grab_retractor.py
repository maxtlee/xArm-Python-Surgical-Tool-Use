def connect_arm(ip: str) -> XArmAPI:
    arm = XArmAPI(ip, do_not_open=True)
    arm.connect()
    if arm.error_code != 0:
        arm.clean_error()
    arm.motion_enable(enable=True)
    arm.set_mode(0)
    arm.set_state(0)
    time.sleep(0.5)
    print(f"[xArm] Connected to {ip}")
    return arm


def go_home(arm: XArmAPI) -> None:
    print("[xArm] Moving to home …")
    arm.set_servo_angle(angle=HOME_JOINTS, speed=30, wait=True)


def move_hover(arm: XArmAPI, pos_mm: np.ndarray) -> bool:
    """
    Move to a hover pose HOVER_CLEARANCE_MM above the target position.
    Returns True on success, False if the arm reports an error.
    """
    hover_z = float(pos_mm[2]) + HOVER_CLEARANCE_MM
    x, y = float(pos_mm[0]), float(pos_mm[1])

    print(f"[xArm] Moving to hover ({x:.1f}, {y:.1f}, {hover_z:.1f}) mm …")
    code = arm.set_position(
        x=x, y=y, z=hover_z,
        roll=TARGET_ROLL_DEG,
        pitch=TARGET_PITCH_DEG,
        yaw=TARGET_YAW_DEG,
        speed=SPEED_MM_S,
        wait=True,
    )
    if code not in (0, None):
        print(f"[xArm] WARNING: set_position returned code={code}")
        return False
    print("[xArm] Hover reached.")
    return True


