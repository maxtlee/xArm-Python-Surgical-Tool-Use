# ===========================================================================
# Stability tracker
# ===========================================================================

class StabilityBuffer:
    """
    Collects translation vectors over the last N frames and reports whether
    the marker pose has been stable (low variance) for that window.
    """

    def __init__(self, n: int, thresh_m: float):
        self.n      = n
        self.thresh = thresh_m
        self.tvecs  = deque(maxlen=n)
        self.rvecs  = deque(maxlen=n)

    def reset(self):
        self.tvecs.clear()
        self.rvecs.clear()

    def update(self, rvec: np.ndarray, tvec: np.ndarray):
        self.tvecs.append(tvec.flatten().copy())
        self.rvecs.append(rvec.flatten().copy())

    def is_stable(self) -> bool:
        if len(self.tvecs) < self.n:
            return False
        arr = np.array(self.tvecs)
        return float(np.max(np.std(arr, axis=0))) < self.thresh

    def mean_pose(self):
        """Return (mean_rvec, mean_tvec) averaged over the buffer."""
        return (
            np.mean(self.rvecs, axis=0),
            np.mean(self.tvecs, axis=0),
        )


# ===========================================================================
# Homogeneous transform helpers
# ===========================================================================

def rvec_tvec_to_T(rvec: np.ndarray, tvec: np.ndarray) -> np.ndarray:
    """Convert a Rodrigues rotation vector + translation vector to a 4×4 transform."""
    R, _ = cv2.Rodrigues(rvec)
    T = np.eye(4, dtype=np.float64)
    T[:3, :3] = R
    T[:3,  3] = tvec.flatten()
    return T


def make_offset_T(offset_xyz: np.ndarray) -> np.ndarray:
    """Return a 4×4 pure-translation homogeneous matrix."""
    T = np.eye(4, dtype=np.float64)
    T[:3, 3] = offset_xyz
    return T


